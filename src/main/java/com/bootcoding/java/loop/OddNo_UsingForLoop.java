package com.bootcoding.java.loop;

public class OddNo_UsingForLoop
    {
        public static void main(String [] args)
        {
            // WAP to print odd number from 1 to given number.....
            // using for loop......
            int i = 25;
            for(i = 1; i <= 25; i++)
            {
                if(i % 2 != 0)
                {
                    System.out.println(i);
                }

            }

        }

    }




