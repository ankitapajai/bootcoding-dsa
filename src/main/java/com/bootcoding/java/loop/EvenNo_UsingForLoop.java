package com.bootcoding.java.loop;

public class EvenNo_UsingForLoop
    {
        public static void main(String [] args)
        {
            // WAP to print even number from 1 to given number.......
            // for loop...
            for(int i = 0; i <= 25; i++)
            {
                if(i % 2 == 0)
                {
                    System.out.println(i);
                }

            }

        }

    }






